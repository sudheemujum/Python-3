#List equality & relational
#Equality operators
l1=['Dog','Cat','Rat']
l2=['Dog','Cat','Rat']
l3=['DOG','CAT','RAT']
l4=['Dog','Rat','Cat']

print('Equality operator')
print(l1==l2)
print(l1==l3)
print(l1==l4)

print(l1!=l2)
print(l1!=l3)
print(l1!=l4)

l1=[10,20,30,40]
l2=[10,20,50]

print('Relational operator')
print(l1<l2)
print(l1>l2)
print(l1==l2)
print(l1!=l2)
